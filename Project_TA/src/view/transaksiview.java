    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
            try {
                detail_transaksi dp = new detail_transaksi();
                dp.setJumlah(Integer.parseInt(this.tfJumlah.getText()));
                dp.setBarang(this.controller.getDataBarang().get(this.cbProduk.getSelectedIndex()));
                Harga_Total += this.controller.getDataBarang().get(cbProduk.getSelectedIndex()).getHarga_satuan()* Integer.parseInt(tfJumlah.getText());
                this.setHargaTotal();
                this.arrKeranjang.add(dp);
                this.showTabelKeranjang();
                this.tfJumlah.setText("");
                
            } catch (SQLException ex) {
                Logger.getLogger(transaksiview.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        transaksi transaksi = new transaksi();
            try {
                transaksi.setPegawai(this.controller.getDataPegawai().get(this.cbPegawai.getSelectedIndex()));
                transaksi.setTanggal_transaksi(new Date());
                transaksi.setHarga_total(this.Harga_Total);
                transaksi.setArrDetail_Transaksi(this.arrKeranjang);
                this.controller.insertTransaksi(transaksi);
                this.showTabelTransaksi();
                this.arrKeranjang = new ArrayList<>();
                this.showTabelKeranjang();
                this.Harga_Total = 0.0;
                this.setHargaTotal();
                this.showComboBoxProduk();
            } catch (SQLException err) {
                System.out.println(err);
            }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnLihatDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLihatDetailActionPerformed
            try {
                transaksi transaksi = this.controller.getDataTransaksi().get(tblPesanan.getSelectedRow());
                String text = ""
                        + "========================= PEGAWAI =============================="+ "\n"
                        + "NAMA : " + transaksi.getPegawai().getNama() + "\n"
                        + "ALAMAT : " + transaksi.getPegawai().getAlamat() + "\n"
                        + "NO TELP : " + transaksi.getPegawai().getNo_telp()+ "\n"
                        + "======================= PEMESANAN =============================="+ "\n"
                        + "TANGGAL TRANSAKSI : " + new SimpleDateFormat("dd/MM/yyyy").format(transaksi.getTanggal_transaksi()) + "\n"
                        + "HARGA TOTAL = " + transaksi.getHarga_total()+ "\n\n"
                        + "======================== PRODUK =============================="+ "\n" ;
                ArrayList<detail_transaksi> dp = transaksi.getArrDetail_Transaksi();
                for(int i=0;i < dp.size();i++){
                    text += "\n" + (i+1) + ". \n"
                            + "NAMA BARANG          : " + dp.get(i).getBarang().getNama_barang()+ "\n"
                            + "HARGA SATUAN         : " + dp.get(i).getBarang().getHarga_satuan()+ "\n"
                            + "STOK BARANG         : " + dp.get(i).getBarang().getStok()+ "\n"
                            + "JUMLAH BELI PRODUK   : " + dp.get(i).getJumlah() + "\n";
                }
                this.taDetail_Pemesanan.setText(text);
            } catch (SQLException ex) {
                Logger.getLogger(transaksiview.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_btnLihatDetailActionPerformed

