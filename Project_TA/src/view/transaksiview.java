    private void showComboBoxPegawai() throws SQLException {
        DefaultComboBoxModel dtmPegawai = new DefaultComboBoxModel();
        for(pegawai p : this.controller.getDataPegawai()){
            dtmPegawai.addElement(p.getNama());
        }
        
        this.cbPegawai.setModel(dtmPegawai);
    }
            
        private void showComboBoxProduk() throws SQLException {
        DefaultComboBoxModel dtmProduk = new DefaultComboBoxModel();
        for(barang p : this.controller.getDataBarang()){
            dtmProduk.addElement(p.getNama_barang()+ " ( " + p.getStok()+ " )");
        }
        this.cbProduk.removeAll();
        this.cbProduk.setModel(dtmProduk);
    }
        
        private void showTabelKeranjang(){
            DefaultTableModel dtmKeranjang = new DefaultTableModel(new String[]{"NAMA PRODUK", "HARGA SATUAN", "JUMLAH"},0);
            dtmKeranjang.setRowCount(0);
            
            for(detail_transaksi dp : this.arrKeranjang){
                dtmKeranjang.addRow(new String[]{dp.getBarang().getNama_barang().toString(), dp.getBarang().getHarga_satuan().toString(), dp.getJumlah().toString()});
            }
            
            this.tblKeranjang.setModel(dtmKeranjang);
        }
        
        private void setHargaTotal(){
            this.tfTotalHarga.setText(String.valueOf(this.Harga_Total));
        }
        
        private void showTabelTransaksi() throws SQLException{
            DefaultTableModel dtmPemesanan = new DefaultTableModel(new String[]{"PEGAWAI", "TANGGAL TANSAKSI", "HARGA_TOTAL"},0);
            dtmPemesanan.setRowCount(0);
            
            for(transaksi p : this.controller.getDataTransaksi()){
                dtmPemesanan.addRow(new String[]{p.getPegawai().getNama(), p.getTanggal_transaksi().toString(), p.getHarga_total().toString()});
            }
            this.tblPesanan.setModel(dtmPemesanan);
        }

