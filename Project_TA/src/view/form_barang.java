    private void showComboBoxPegawai() throws SQLException {
        DefaultComboBoxModel dtmPegawai = new DefaultComboBoxModel();
        for(pegawai p : this.controller.getDataPegawai()){
            dtmPegawai.addElement(p.getNama());
        }
        
        this.cbPegawai.setModel(dtmPegawai);
    }
    
    private void showTabelBarang() throws SQLException{
            DefaultTableModel dtmBarang = new DefaultTableModel(new String[]{"ID_BARANG", "NAMA PEGAWAI", "NAMA BARANG", "HARGA SATUAN", "STOK"},0);
            dtmBarang.setRowCount(0);
            
            for(barang b : this.controller.getDataBarang()){
                dtmBarang.addRow(new String[]{b.getId_barang().toString(), b.getPegawai().getNama(), b.getNama_barang(), b.getHarga_satuan().toString(), b.getStok().toString()});
            }
            this.tbBarang.setModel(dtmBarang);
        }
    
    

