    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        barang barang = new barang();
            try {
                barang.setPegawai(this.controller.getDataPegawai().get(this.cbPegawai.getSelectedIndex()));
                barang.setNama_barang(this.tfNama.getText());
                barang.setHarga_satuan(Integer.parseInt(this.tfHarga.getText()));
                barang.setStok(Integer.parseInt(this.tfStok.getText()));
                this.controller.insertBarang(barang);
                this.showTabelBarang();
                this.kosong();
            } catch (SQLException err) {
                System.out.println(err);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

